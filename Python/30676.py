d = int(input())

if 620 <= d <= 780:
    print("Red")
elif 590 <= d < 620:
    print("Orange")
elif 570 <= d < 590:
    print("Yellow")
elif 495 <= d < 570:
    print("Green")
elif 450 <= d < 495:
    print("Blue")
elif 425 <= d < 450:
    print("Indigo")
elif 380 <= d < 425:
    print("Violet")